package sample;

public class Constructor {
	
		int z;
		
		
		public Constructor(int hhh)  {
		  z = hhh;
		}
		
		
		
	public void addingTwoNumb(int a, int b) {
		int tot = a + b + z;
		System.out.println(tot);
	}
	
	
	
        //Function with arguments(parameters) and being dynamic
      
     public void addingThreeNumb(int a, int b, int c) {
    	 int tot = a + b + c + z;
    	 System.out.println(tot);
     }
     
     
     public void addingFourNumb(int a, int b, int c, int d) {
    	 int tot = a + b + c + d + z;
    	 System.out.println(tot);
    	 
     }
     
     
     
	
	

}
